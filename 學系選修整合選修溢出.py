def run_optimization_audit(pdf_path):
    all_rows = []
    with pdfplumber.open(pdf_path) as pdf:
        for page in pdf.pages:
            table = page.extract_table(table_settings={"vertical_strategy": "lines", "horizontal_strategy": "lines"})
            if table: all_rows.extend(table)

    df = pd.DataFrame(all_rows)
    if df.shape[1] > 11: df = df.iloc[:, :11]
    df.columns = ['類別', '科目_左', '課程名稱_左', '學分_左', '修課學期_右', '科目_右', '課程名稱_右', '性質_右', '學期_右', '學分_右', '分數_右']
    df['搜尋包'] = df.apply(lambda row: "".join([str(i) for i in row if i]).replace(" ", "").replace("\n", ""), axis=1)

    try:
        # 動態抓取門檻與定位
        t_row = df[df['搜尋包'].str.contains("學系選修：", na=False)].iloc[0]
        major_req = float(re.search(r"(\d+)", "".join([str(t_row[i]) for i in range(5) if t_row[i]])).group(1))
        m_start = df[df['搜尋包'].str.contains("學系必修：", na=False)].index[0]
        m_end = df[df['搜尋包'].str.contains("學系選修：", na=False)].index[0]
        
        major_pool = []
        intern_total = 0
        for _, row in df.iloc[m_start+1 : m_end].iterrows():
            if str(row['分數_右']).strip().lower() not in ['', 'none', 'nan', 'pd.na']:
                raw_name = str(row['課程名稱_右']).replace("\n", "").strip()
                clean = smart_normalize(raw_name)
                if any(k in clean for k in INTERNSHIP_KEYWORDS):
                    intern_total += float(row['學分_右'])
                else:
                    # 修正語法錯誤：移除字典賦值中的 :=
                    major_pool.append({
                        "raw": raw_name, "clean": clean, 
                        "credits": float(row['學分_右']), "markers": count_markers(raw_name)
                    })
        
        # 實習學分平權加入
        if intern_total > 0:
            major_pool.append({"raw": "實習課程(累計)", "clean": "實習課程(累計)", "credits": min(intern_total, 4.0), "markers": count_markers("(就)暑期實習")})

        # --- 多學程尋優模擬 ---
        simulations = []
        for p_name, p_data in PROGRAMS.items():
            base_list = [smart_normalize(b) for b in p_data["基礎"]]
            ext_list = [smart_normalize(e) for e in p_data["選修、橫跨"]]
            candidates = [c for c in major_pool if c['clean'] in base_list or c['clean'] in ext_list]
            
            locked, locked_sum = find_best_pit_filler(candidates, base_list)
            if locked_sum >= 10.0:
                total_major = sum(c['credits'] for c in major_pool)
                overflow_limit = max(0, total_major - major_req)
                rem = [c for c in major_pool if c not in locked]
                rem.sort(key=lambda x: (-x['markers'], -x['credits']))
                
                sim_free_sum = 0
                for c in rem:
                    if c['markers'] > 0 and (sim_free_sum + c['credits'] <= overflow_limit):
                        sim_free_sum += c['credits']
                simulations.append({"name": p_name, "locked": locked, "free_sum": sim_free_sum})

        if not simulations:
            return print("\n⚠️ 目前尚未滿足任何學程門檻。")

        # 排序選出能產出最多自由學分的學程
        simulations.sort(key=lambda x: -x['free_sum'])
        best_p = simulations[0]

        print("\n" + "═"*95)
        print(f"║ {'學系選修尋優報告：鎖定【' + best_p['name'] + '】以極大化自由學分':^68} ║")
        print("═"*95)
        
        total_major_earned = sum(c['credits'] for c in major_pool)
        final_overflow = max(0, total_major_earned - major_req)
        final_rem = [c for c in major_pool if c not in best_p['locked']]
        final_rem.sort(key=lambda x: (-x['markers'], -x['credits']))
        
        final_free_list = []
        final_free_sum = 0
        for c in final_rem:
            if c['markers'] > 0 and (final_free_sum + c['credits'] <= final_overflow):
                final_free_list.append(c)
                final_free_sum += c['credits']

        print(f"{'狀態':<18} | {'課程名稱':<45} | {'學分'}")
        print("-" * 95)
        for c in major_pool:
            if c in final_free_list: status = "✅ 自由選修"
            elif c in best_p['locked']: status = f"🔒 鎖定({best_p['name'][:2]})"
            else: status = "📌 一般保留"
            print(f"{status:<18} | {c['raw']:<45} | {c['credits']:>4.1f}")
            
        print("-" * 95)
        print(f"║ {'>>> 此學系選修區塊撥入自由學分總計 <<<':^64} ║")
        print(f"║ {final_free_sum:^83.1f} 學分 ║")
        print("═"*95 + "\n")

    except Exception as e:
        print(f"❌ 錯誤: {e}")

